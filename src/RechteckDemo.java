public class RechteckDemo {
    public static void main(String[] args) {


        int laenge;
        int breite;
        breite = 20;
        laenge = 10;

        int umfang = (2 * laenge) + (2 * breite);
        int flaeche = laenge * breite;


        System.out.println("Der Umfang des Rechtecks ist: " + umfang);
        System.out.println("Der Inhalt des Rechtecks ist: " + flaeche);


    }
}
